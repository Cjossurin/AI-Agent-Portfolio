    def _load_performance_data(self) -> str:
        """
        Load the most recent analytics report for this client from disk and
        return a formatted string describing last week's top-performing
        content so it can be injected into the Claude content-ideas prompt.
        Returns empty string when no report is available.
        """
        try:
            import glob as _glob
            report_dir = os.path.join("storage", "analytics", self.client_id)
            if not os.path.isdir(report_dir):
                return ""
            report_files = sorted(
                _glob.glob(os.path.join(report_dir, "report_*.json")),
                reverse=True,
            )
            if not report_files:
                return ""
            with open(report_files[0]) as _f:
                report = json.load(_f)

            lines = []
            best = report.get("best_platform", "")
            if best:
                lines.append(f"Best performing platform last week: {best}")

            for p in report.get("platforms", []):
                name = p.get("platform", "")
                er   = p.get("engagement_rate", 0)
                al   = p.get("avg_likes", 0)
                ac   = p.get("avg_comments", 0)
                top  = p.get("top_post_id", "")
                entry = (f"  {name}: engagement_rate={er:.2f}%, "
                         f"avg_likes={al:.0f}, avg_comments={ac:.0f}")
                if top:
                    entry += f" (top post id: {top})"
                lines.append(entry)

            for rec in report.get("recommendations", [])[:3]:
                lines.append(f"Performance insight: {rec}")

            return "\n".join(lines) if lines else ""
        except Exception:
            return ""

    def _get_client_context(self, niche: str, additional_context: str = "") -> str:
        """Retrieve client context from RAG — always tries the knowledge base first."""
        # Build a prioritised list of queries: business name beats niche alone
        biz_name = ""
        if self.client_profile:
            biz_name = getattr(self.client_profile, "business_name", "") or ""
        queries = [q for q in [biz_name, niche, f"{niche} content strategy", "content strategy"] if q]
        # Deduplicate while preserving order
        seen: set = set()
        unique_queries = [q for q in queries if not (q in seen or seen.add(q))]

        print(f"🔍 RAG lookup for [{self.client_id}] — {len(unique_queries)} queries: {unique_queries}")

        for i, query in enumerate(unique_queries, 1):
            try:
                results = self.rag.search(query, self.client_id, limit=5, score_threshold=0.2)
                print(f"   Query {i}/{len(unique_queries)}: '{query}' → {len(results)} results (threshold=0.2)")
                if results:
                    context = "\n".join([f"- {r['text'][:300]}" for r in results])
                    print(f"✅ RAG context loaded for [{self.client_id}] via query='{query}' ({len(results)} chunks)")
                    return f"Client Knowledge:\n{context}\n\n{additional_context}"
            except Exception as e:
                print(f"⚠️ RAG search failed (query='{query}'): {e}")

        # Absolute last-resort: score_threshold=0.0 returns best matches regardless of score
        fallback_query = biz_name or niche
        try:
            results = self.rag.search(fallback_query, self.client_id, limit=3, score_threshold=0.0)
            print(f"   Absolute fallback query '{fallback_query}' (threshold=0.0) → {len(results)} results")
            if results:
                context = "\n".join([f"- {r['text'][:300]}" for r in results])
                print(f"✅ RAG context loaded for [{self.client_id}] via absolute fallback ({len(results)} chunks)")
                return f"Client Knowledge:\n{context}\n\n{additional_context}"
        except Exception as e:
            print(f"⚠️ RAG absolute fallback failed: {e}")

        print(f"❌ No RAG results for [{self.client_id}] across all thresholds — check if knowledge docs are indexed")

        # Build a structured profile fallback from the client profile fields
        # so Claude always has rich business context even without RAG docs.
        profile_context = self._build_profile_context(niche)
        parts = [p for p in [profile_context, additional_context] if p]
        return "\n\n".join(parts) if parts else f"Business Niche: {niche}"
    
    def _build_profile_context(self, niche: str) -> str:
        """Assemble a structured context block from the client profile fields.

        Called when RAG returns no results so Claude still understands the
        business beyond a bare niche label.  Every field is pulled
        dynamically from the profile — nothing is hardcoded per niche.
        """
        if not self.client_profile:
            return f"Business Niche: {niche}"

        lines = []
        biz_name = getattr(self.client_profile, "business_name", None) or getattr(self.client_profile, "client_name", None)
        if biz_name:
            lines.append(f"Business Name: {biz_name}")
        lines.append(f"Niche / Industry: {niche}")
        biz_desc = getattr(self.client_profile, "business_description", None)
        if biz_desc:
            lines.append(f"About the Business:\n{biz_desc}")
        tone = getattr(self.client_profile, "tone", None)
        if tone:
            lines.append(f"Brand Voice / Tone: {tone}")
        kw = getattr(self.client_profile, "keywords", None)
        if kw and isinstance(kw, list) and kw:
            lines.append(f"Key Topics & Keywords: {', '.join(kw)}")
        pillars = getattr(self.client_profile, "content_pillars", None)
        if pillars and isinstance(pillars, list) and pillars:
            lines.append(f"Content Pillars: {', '.join(pillars)}")
        platforms = getattr(self.client_profile, "platforms", None)
        if platforms and isinstance(platforms, list) and platforms:
            lines.append(f"Active Platforms: {', '.join(platforms)}")

        if not lines:
            return f"Business Niche: {niche}"
        return "**CLIENT BUSINESS PROFILE:**\n" + "\n".join(lines)

    def _get_deep_research_context(self, platforms: List[str], themes: List[str] = None) -> str:
        """
        Retrieve deep research insights from the KnowledgeBase.
        
        Searches for platform-specific algorithm knowledge, viral patterns,
        and strategy frameworks to inform content idea generation.
        """
        research_context = ""
        search_queries = []
        
        # Platform-specific algorithm research
        platform_queries = {
            "instagram": "Instagram algorithm 2025 engagement reach Reels",
            "tiktok": "TikTok viral video patterns FYP algorithm hooks",
            "linkedin": "LinkedIn B2B content strategy algorithm posts",
            "facebook": "Facebook algorithm 2025 organic reach engagement",
            "youtube": "YouTube algorithm trending video optimization",
            "twitter": "Twitter algorithm engagement viral tweets"
        }
        
        # Add platform queries
        for platform in platforms:
            if platform.lower() in platform_queries:
                search_queries.append(platform_queries[platform.lower()])
        
        # Add general strategy queries
        search_queries.extend([
            "viral content patterns hooks storytelling",
            "content pillar strategy mix educational entertaining",
            "hashtag strategy optimal best practices",
            "posting times optimal engagement"
        ])
        
        # Add theme-specific queries
        if themes:
            for theme in themes[:3]:  # Limit to avoid token overflow
                search_queries.append(f"{theme} content strategy tactics")
        
        print(f"\n📚 Searching deep research knowledge base...")
        
        all_insights = []
        seen_texts = set()  # Deduplicate
        
        for query in search_queries[:6]:  # Limit queries to control API costs
            try:
                results = self.knowledge_base.search(
                    query=query,
                    tags=["deep_research"],
                    limit=2
                )
                
                for r in results:
                    # Deduplicate by first 100 chars
                    text_key = r["text"][:100]
                    if text_key not in seen_texts and r["score"] > 0.75:
                        seen_texts.add(text_key)
                        all_insights.append({
                            "text": r["text"][:500],  # Truncate for prompt
                            "score": r["score"],
                            "tags": r["tags"]
                        })
            except Exception as e:
                print(f"   ⚠️ Search error for '{query[:30]}': {e}")
        
        # Sort by relevance and take top insights
        all_insights.sort(key=lambda x: x["score"], reverse=True)
        top_insights = all_insights[:5]
        
        if top_insights:
            print(f"   ✅ Found {len(top_insights)} relevant research insights")
            research_context = "\n**DEEP RESEARCH INSIGHTS (Algorithm & Strategy Knowledge):**\n"
            for i, insight in enumerate(top_insights, 1):
                # Clean up the text
                text = insight["text"].replace("\n", " ").strip()
                research_context += f"\n{i}. {text}\n"
        else:
            print(f"   ℹ️ No deep research found (run: python agents/knowledge_base.py ingest ...)")
        
        return research_context
    
