    async def get_meta_insights(self, page_id: str) -> Dict:
        """
        Get client's Facebook/Instagram performance insights using Meta Graph API.
        
        Args:
            page_id: Facebook Page ID
        
        Returns:
            Dictionary with best posts, optimal times, audience data
        """
        if not self.meta_access_token:
            print("⚠️ Meta API not configured. Set INSTAGRAM_ACCESS_TOKEN in .env")
            return {}
        
        try:
            print(f"📊 Fetching Meta insights for page: {page_id}")
            
            # Get page posts with insights
            url = f"https://graph.facebook.com/v21.0/{page_id}/posts"
            params = {
                "access_token": self.meta_access_token,
                "fields": "id,message,created_time,insights.metric(post_impressions,post_engaged_users)",
                "limit": 25
            }
            
            response = requests.get(url, params=params)
            data = response.json()
            
            if "error" in data:
                print(f"❌ Meta API error: {data['error'].get('message')}")
                return {}
            
            posts = data.get("data", [])
            
            # Analyze top posts
            top_posts = []
            for post in posts[:10]:
                insights = post.get("insights", {}).get("data", [])
                engagement = 0
                impressions = 0
                
                for metric in insights:
                    if metric.get("name") == "post_engaged_users":
                        engagement = metric.get("values", [{}])[0].get("value", 0)
                    elif metric.get("name") == "post_impressions":
                        impressions = metric.get("values", [{}])[0].get("value", 0)
                
                top_posts.append({
                    "message": post.get("message", "")[:200],
                    "engagement": engagement,
                    "impressions": impressions,
                    "created_time": post.get("created_time", "")
                })
            
            # Sort by engagement
            top_posts.sort(key=lambda x: x["engagement"], reverse=True)
            
            print(f"✅ Analyzed {len(top_posts)} posts from Meta")
            return {
                "top_posts": top_posts[:5],
                "total_posts_analyzed": len(posts)
            }
            
        except Exception as e:
            print(f"❌ Meta API error: {e}")
            return {}
    
