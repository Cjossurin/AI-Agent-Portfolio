def _repair_truncated_json(raw: str) -> str:
    """Best-effort repair of JSON that was truncated mid-string by max_tokens.

    Handles common cases:
    - Unterminated string: close the open quote
    - Unbalanced brackets/braces: close them
    - Trailing commas before closers
    """
    # Remove trailing commas before } or ]
    raw = re.sub(r',\s*$', '', raw.rstrip())

    # Count open/close braces and brackets
    in_str = False
    esc = False
    opens = []
    for ch in raw:
        if esc:
            esc = False
            continue
        if ch == '\\':
            esc = True
            continue
        if ch == '"':
            in_str = not in_str
            continue
        if in_str:
            continue
        if ch in ('{', '['):
            opens.append(ch)
        elif ch == '}' and opens and opens[-1] == '{':
            opens.pop()
        elif ch == ']' and opens and opens[-1] == '[':
            opens.pop()

    # If we're inside a string, close it
    if in_str:
        raw += '"'

    # Close any remaining open braces/brackets
    for opener in reversed(opens):
        raw += ']' if opener == '[' else '}'

    # Final trailing comma cleanup
    raw = re.sub(r',\s*([}\]])', r'\1', raw)
    return raw


