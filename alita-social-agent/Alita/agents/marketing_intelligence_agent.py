@dataclass
class ContentIdea:
    """
    A single content idea generated by the Marketing Intelligence Agent.
    This is the primary output consumed by other agents.
    """
    idea_id: str
    topic: str
    angle: str                          # The unique perspective/hook
    format: str                         # ContentFormat value
    goal: str                           # ContentGoal value
    recommended_platforms: List[str]    # e.g., ["instagram", "tiktok"]
    hooks: List[str]                    # Opening lines/hooks to use
    keywords: List[str]                 # SEO/hashtag keywords
    priority: str                       # Priority value
    reasoning: str                      # Why this idea was generated
    estimated_engagement: str           # low/medium/high
    best_posting_time: Optional[str] = None
    media_type: str = "image"           # image, video, carousel, none
    call_to_action: Optional[str] = None
    created_at: str = None
    
    def __post_init__(self):
        if self.created_at is None:
            self.created_at = datetime.now().isoformat()
    
    def to_dict(self) -> Dict:
        return asdict(self)
    
    def to_content_request(self) -> Dict:
        """Convert to format expected by ContentCreationAgent"""
        return {
            "platform": self.recommended_platforms[0] if self.recommended_platforms else "instagram",
            "content_type": self.format,
            "topic": f"{self.topic}: {self.angle}",
            "goal": self.goal,
            "hooks": self.hooks,
            "keywords": self.keywords
        }


@dataclass
class ContentStrategy:
    """
    Weekly/monthly content strategy output.
    Contains multiple ContentIdeas organized by day/time.
    """
    strategy_id: str
    client_id: str
    period: str                         # "weekly" or "monthly"
    start_date: str
    end_date: str
    ideas: List[ContentIdea]
    content_mix: Dict[str, int]         # e.g., {"video": 40, "image": 40, "article": 20}
    posting_frequency: int              # Posts per week
    themes: List[str]                   # Overall themes for the period
    notes: str                          # Strategic notes
    created_at: str = None
    
    def __post_init__(self):
        if self.created_at is None:
            self.created_at = datetime.now().isoformat()
    
    def to_dict(self) -> Dict:
        result = asdict(self)
        result["ideas"] = [idea.to_dict() for idea in self.ideas]
        return result


