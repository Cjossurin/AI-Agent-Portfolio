    async def generate_weekly_strategy(
        self,
        niche: Optional[str] = None,
        posts_per_week: Optional[int] = None,
        platforms: Optional[List[str]] = None,
        themes: Optional[List[str]] = None,
        additional_context: str = ""
    ) -> ContentStrategy:
        """
        Generate a complete weekly content strategy.
        
        Args:
            niche: Client's business niche (uses client profile if not provided)
            posts_per_week: Target number of posts per week (uses posting_frequency if not provided)
            platforms: Platforms to post on (uses niche platforms if not provided)
            themes: Themes for the week (uses content pillars if not provided)
            additional_context: Extra context
        
        Returns:
            ContentStrategy with organized ideas for the week
        """
        # Use client profile settings as defaults
        if not niche and self.client_profile:
            niche_value = self.client_profile.niche.value if hasattr(self.client_profile.niche, 'value') else self.client_profile.niche
            niche = niche_value.replace('_', ' ')
        elif not niche:
            niche = "general business"
        
        if not posts_per_week and self.client_profile:
            posts_per_week = self.client_profile.posting_frequency
        elif not posts_per_week:
            posts_per_week = 7
        
        if not platforms and self.client_profile:
            platforms = self.client_profile.platforms
        
        if not themes and self.client_profile and self.client_profile.content_pillars:
            themes = self.client_profile.content_pillars if isinstance(self.client_profile.content_pillars, list) else [self.client_profile.content_pillars]
        
        print(f"\n📅 Generating weekly content strategy for {niche}...")
        print(f"   Posts per week: {posts_per_week}")
        if platforms:
            print(f"   Platforms: {', '.join(platforms)}")
        if themes:
            print(f"   Themes: {', '.join(themes[:3])}...")
        
        # Generate ideas
        ideas = await self.generate_content_ideas(
            niche=niche,
            num_ideas=posts_per_week,
            platforms=platforms,
            themes=themes,
            additional_context=additional_context
        )
        
        # Calculate content mix
        format_counts = {}
        for idea in ideas:
            fmt = idea.format
            format_counts[fmt] = format_counts.get(fmt, 0) + 1
        
        total = len(ideas) or 1
        content_mix = {fmt: int((count / total) * 100) for fmt, count in format_counts.items()}
        
        # Generate strategy
        today = datetime.now()
        strategy = ContentStrategy(
            strategy_id=self._generate_strategy_id(),
            client_id=self.client_id,
            period="weekly",
            start_date=today.strftime("%Y-%m-%d"),
            end_date=(today + timedelta(days=7)).strftime("%Y-%m-%d"),
            ideas=ideas,
            content_mix=content_mix,
            posting_frequency=posts_per_week,
            themes=themes or ["engagement", "education", "promotion"],
            notes=f"Weekly strategy for {niche}. Focus on consistency and engagement."
        )
        
        print(f"✅ Generated weekly strategy with {len(ideas)} content ideas")
        return strategy
    
